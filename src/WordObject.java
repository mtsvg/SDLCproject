public class WordObject {
    String word;
    int count;

    public WordObject(String aWord, int aCount) {
        word = aWord;
        count = aCount;
    }

    public String getWord() {
        return word;
    }

    public void setWord(String word) {
        this.word = word;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

}
